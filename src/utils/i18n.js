/**
 * Custom hook to get the translation function `t` and the current language.
 * Always syncs with the current `localStorage` value.
 */
export const useLanguage = (currentProfile) => {
  const [activeLang, setActiveLang] = useState('en');
  const [profile, setProfile] = useState({});

  useEffect(() => {
    try {
      // Prioritize passed profile, fallback to localStorage
      const p = currentProfile || JSON.parse(localStorage.getItem('etpulse_profile')) || {};
      setProfile(p);
      const userLang = p.preferred_language || (p.languages && p.languages[0]) || 'en';
      
      // Map to supported codes
      const langMap = {
        'English': 'en',
        'en': 'en',
        'Hindi': 'hi',
        'hi': 'hi',
        'Tamil': 'ta',
        'ta': 'ta',
        'Telugu': 'te',
        'te': 'te',
        'Bengali': 'bn',
        'bn': 'bn',
        'Marathi': 'mr',
        'mr': 'mr'
      };
      
      setActiveLang(langMap[userLang] || 'en');
    } catch {
      setActiveLang('en');
    }
  }, [currentProfile]);

  const t = useCallback((key, variables = {}) => {
    const { defaultValue, ...vars } = variables;
    let text = translations[activeLang]?.[key] || translations['en'][key] || defaultValue || key;
    
    // Replace variables like {name}
    Object.keys(vars).forEach(vKey => {
      text = text.replace(new RegExp(`{${vKey}}`, 'g'), vars[vKey]);
    });
    
    return text;
  }, [activeLang]);

  return { t, language: activeLang, fullLanguageName: profile?.preferred_language || 'English' };
};
